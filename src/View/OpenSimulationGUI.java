    private void jButtonOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOpenActionPerformed
		if (jListSimulation.getSelectedIndex() != -1) {
			Simulation Simulation = (Simulation) jListSimulation.
				getSelectedValue();
			if (this.openProjectController.openSimulation(Simulation)) {
				JOptionPane.
					showMessageDialog(this, "Open project successfully!");
				this.close();
			} else {
				JOptionPane.
					showMessageDialog(this, "Error opening project: " + Error.
									  getErrorMessage());
			}
		}
    }//GEN-LAST:event_jButtonOpenActionPerformed

